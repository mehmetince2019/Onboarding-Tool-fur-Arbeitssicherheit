function __($k) {
    global $T, $lang;
    if (isset($T[$lang][$k])) return $T[$lang][$k];
    if (isset($T['de'][$k])) return $T['de'][$k];
    return $k;
}
function render_lang_switcher() {
    $base = strtok($_SERVER['REQUEST_URI'] ?? '', '?');
    $qs = $_GET;
    $mk = function($l) use ($base, $qs) {
        $qs['lang'] = $l;
        return htmlspecialchars($base . '?' . http_build_query($qs));
    };
    return '<div class="d-flex align-items-center gap-2">'
        .'<a class="text-decoration-none" href="'.$mk('de').'" title="Deutsch">🇩🇪</a>'
        .'<a class="text-decoration-none" href="'.$mk('en').'" title="English">🇬🇧</a>'
        .'<a class="text-decoration-none" href="'.$mk('tr').'" title="Türkçe">🇹🇷</a>'
        .'</div>';
}
